#pragma once

namespace wx {

//-----------------------------------------------------------------------------
//! @class	CommandLine
//! @brief	コマンドライン
//-----------------------------------------------------------------------------
class CommandLine
{
public:
	//-------------------------------------------------------------------------
	//! @brief	コンストラクタ
	//-------------------------------------------------------------------------
	CommandLine();

	//-------------------------------------------------------------------------
	//! @brief	デストラクタ
	//-------------------------------------------------------------------------
	virtual ~CommandLine();

	//-------------------------------------------------------------------------
	//! @brief	プログラム名を除く引数の数を取得する
	//!
	//! @return プログラム名を除く引数の数
	//-------------------------------------------------------------------------
	int GetArgc() const;

	//-------------------------------------------------------------------------
	//! @brief	プログラム名を除く引数を取得する
	//!
	//! @param	pos			引数位置
	//!
	//! @return プログラム名を除く引数の文字列
	//-------------------------------------------------------------------------
	const wchar_t* GetArgv(int pos) const;

	//-------------------------------------------------------------------------
	//! @brief	プログラム名引数を取得する
	//!
	//! @return プログラム名引数の文字列
	//-------------------------------------------------------------------------
	const wchar_t* GetExeName() const;

private:
	wchar_t**	m_argv;
	int			m_argc;
};

} //namespace wx
